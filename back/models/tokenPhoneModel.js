'use strict';
//////////////////////////////////////////////////////////////////////
///////////***********     llamo a moongoose        ****//////////////
//////////////////////////////////////////////////////////////////////
let mongoose = require('mongoose');
let Schema   = mongoose.Schema;

//////////////////////////////////////////////////////////////////////////////
////////***********     creo el esquema / categorias        ****//////////////
//////////////////////////////////////////////////////////////////////////////
const geoSchema = mongoose.Schema({
	type:{
		type:String,
		default:"Point"
	},
	coordinates:{
		type:[Number],
		index:'2dsphere'
	}
})

let TokenPhone = new Schema({
	tokenPhone: String,
	creado    : String,
	update    : String,
	showNotificacion:{ type : Boolean, default:true},
	loc 	  : geoSchema,
});

//////////////////////////////////////////////////////////////////////////////
////////***********    exporto el esquema        ****/////////////////////////
//////////////////////////////////////////////////////////////////////////////
module.exports= mongoose.model('TokenPhone', TokenPhone)


 